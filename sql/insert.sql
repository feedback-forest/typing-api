INSERT INTO typing (member_id, phrase_id, acc, cpm, max_cpm, score, wpm, created_date, modified_date)
SELECT
    m.id AS member_id,
    FLOOR(RAND() * 280) + 1 AS phrase_id,
    ROUND(0.75 + RAND() * 0.20, 2) AS acc,
    ROUND(200 + RAND() * 180) AS cpm,
    ROUND(200 + RAND() * 180) + ROUND(20 + RAND() * 50) AS max_cpm,
    ROUND(200 + RAND() * 100) AS score,
    ROUND(40 + RAND() * 50) AS wpm,
    DATE_SUB(NOW(), INTERVAL FLOOR(RAND() * 90) DAY) AS created_date,
    NOW() AS modified_date
FROM
    member m
        CROSS JOIN
    (SELECT 1 AS n UNION SELECT 2 UNION SELECT 3 UNION SELECT 4 UNION SELECT 5 UNION SELECT 6 UNION SELECT 7 UNION SELECT 8 UNION SELECT 9 UNION SELECT 10) seq1
        CROSS JOIN
    (SELECT 1 AS n UNION SELECT 2 UNION SELECT 3 UNION SELECT 4 UNION SELECT 5 UNION SELECT 6 UNION SELECT 7 UNION SELECT 8 UNION SELECT 9 UNION SELECT 10) seq2;
